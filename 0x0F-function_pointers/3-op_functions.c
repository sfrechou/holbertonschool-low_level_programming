#include "3-calc.h"

/**
 * op_add - Entry point
 * @a: s
 * @b: s
 *
 *
 * Return: Always 0 (Success)
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Entry point
 * @a: s
 * @b: s
 *
 *
 * Return: Always 0 (Success)
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Entry point
 * @a: s
 * @b: s
 *
 *
 * Return: Always 0 (Success)
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Entry point
 * @a: s
 * @b: s
 *
 *
 * Return: Always 0 (Success)
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Entry point
 * @a: s
 * @b: s
 *
 *
 * Return: Always 0 (Success)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
